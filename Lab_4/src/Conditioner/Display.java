package Conditioner;
/**
 * Class Display
 * @version 1.0
 */
public class Display {

    int temp = 0;
    /**
     * Method for setting temperature on display
     * @param temp
     */
    public void setTemp(int temp)
    {
        this.temp = temp;
    }
    /**
     * Method for getting temperature from display
     */
    public int getTemp()
    {
        return temp;
    }
	
	

}
